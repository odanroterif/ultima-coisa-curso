    private void send_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_send_buttonActionPerformed
        vehicle mobil = new  vehicle();
        vacancy day = new vacancy();
        connect_manager co_man = new connect_manager();        
        String plate = plate_field.getText();        
                
        
        if (plate.length() > 5)
        {
            plate = plate.substring(0,4);
            mobil.setPlate(plate);
        }
        else
        {
            mobil.setPlate(plate);
        }
                
                
        mobil.setColor(color_field.getText());
        mobil.setModel(model_field.getText());
        day.setDisponibilit(service_field.getText());
        
        
        co_man.sign_in(mobil,day);
    }//GEN-LAST:event_send_buttonActionPerformed

